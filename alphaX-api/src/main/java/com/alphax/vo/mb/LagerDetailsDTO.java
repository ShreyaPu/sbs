package com.alphax.vo.mb;

public class LagerDetailsDTO {
	
	private String warehouseNo;

	private String WarehouseName;

	private String address;
	
	private String vfNumber;
	
	private String warehouseIdWithName;
	
	public LagerDetailsDTO() {

	}


	/**
	 * @return the warehouseNo
	 */
	public String getWarehouseNo() {
		return warehouseNo;
	}


	/**
	 * @param warehouseNo the warehouseNo to set
	 */
	public void setWarehouseNo(String warehouseNo) {
		this.warehouseNo = warehouseNo;
	}


	/**
	 * @return the warehouseName
	 */
	public String getWarehouseName() {
		return WarehouseName;
	}


	/**
	 * @param warehouseName the warehouseName to set
	 */
	public void setWarehouseName(String warehouseName) {
		WarehouseName = warehouseName;
	}


	/**
	 * @return the address
	 */
	public String getAddress() {
		return address;
	}


	/**
	 * @param address the address to set
	 */
	public void setAddress(String address) {
		this.address = address;
	}


	/**
	 * @return the vfNumber
	 */
	public String getVfNumber() {
		return vfNumber;
	}


	/**
	 * @param vfNumber the vfNumber to set
	 */
	public void setVfNumber(String vfNumber) {
		this.vfNumber = vfNumber;
	}


	/**
	 * @return the warehouseIdWithName
	 */
	public String getWarehouseIdWithName() {
		return warehouseIdWithName;
	}


	/**
	 * @param warehouseIdWithName the warehouseIdWithName to set
	 */
	public void setWarehouseIdWithName(String warehouseIdWithName) {
		this.warehouseIdWithName = warehouseIdWithName;
	}


	

	
}
